	<header><strong>Position the Screen</strong></header>
The Eyegaze Edge screen must be positioned in front of the user in such a way that a) he can comfortably see the entire screen, and b) the camera can clearly “see” one of his eyes. He should be positioned between 16 – 31 inches from the screen, depending on his personal preference. The screen should always be parallel to his face with the top edge no higher than eye level.If the user is reclining, angle the top of the screen down so he is facing it. If he is side-lying it is typically easiest to point the camera at the eye which is highest.
